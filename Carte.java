    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            
          String nume = txtNume.getText();
          ItemCategorie catItem = (ItemCategorie) txtCategorie.getSelectedItem();
          ItemAutor autItem = (ItemAutor) txtAutor.getSelectedItem();
          
          String pagini = txtPagini.getText();
          String editie = txtEditia.getText();
          
            pst = con.prepareStatement("Insert into carte(numeCarte,categoria,autor,nrPagini,editia)values(?,?,?,?,?)");
            pst.setString(1, nume);
            pst.setInt(2,catItem.id);
            pst.setInt(3,autItem.id);
            pst.setString(4, pagini);
            pst.setString(5, editie);
            int k = pst.executeUpdate();
            if (k == 1) {
                JOptionPane.showMessageDialog(this,"Ai adaugat o carte");
               txtNume.setText("");
               txtCategorie.setSelectedItem(-1);
               txtAutor.setSelectedItem(-1);
               txtPagini.setText("");
               txtEditia.setText("");
        
                Carte_Load();
                
            }
            else {
                JOptionPane.showMessageDialog(this,"Eroare");
            }} catch (SQLException ex) {
            Logger.getLogger(Carte.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

