    public void Carte_Load() {
        int c;
        try {
            pst = con.prepareStatement("select b.id,b.numeCarte,c.NumeCateg,a.nume,b.nrPagini,b.editia from carte b JOIN categorie c ON b.categoria = c.id JOIN autor a ON b.autor = a.id");
            rs = pst.executeQuery();
            ResultSetMetaData rsd = (ResultSetMetaData) rs.getMetaData();
            c = rsd.getColumnCount();
            
            DefaultTableModel d = (DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            
            while (rs.next())
            {
                Vector v2 = new Vector();
                for(int i= 1; i<=c; i++)
                {
                   v2.add(rs.getString("b.id"));
                   v2.add(rs.getString("b.numeCarte"));
                   v2.add(rs.getString("c.NumeCateg"));
                   v2.add(rs.getString("a.nume"));
                   v2.add(rs.getString("b.nrPagini"));
                   v2.add(rs.getString("b.editia"));
                   
                   
                   
                }
                d.addRow(v2);
            } 
        } catch (SQLException ex) {
            Logger.getLogger(Carte.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    
