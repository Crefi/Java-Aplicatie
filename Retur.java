    private void txtMidKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMidKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
        String id = txtMid.getText();
        try {
          
            pst = con.prepareStatement("select m.Nume,b.numeCarte,i.dataRetur,DATEDIFF(now(),i.dataRetur) as timp from imprumut i JOIN membru m ON i.membruId = m.id JOIN carte b ON i.carteId = b.id and i.membruId = ?");
            pst.setString(1, id);
            rs = pst.executeQuery();
            if(rs.next() == false) {
                JOptionPane.showMessageDialog(this, "Id membru nu a fost gasit");
                
            }
            else {
                String mNume = rs.getString("m.Nume");
                String bNume = rs.getString("b.numeCarte");
                
                txtMnume.setText(mNume.trim());
                txtCarte.setText(bNume.trim());
                
                String data = rs.getString("i.dataRetur");
                txtData.setText(data);
                String timp = rs.getString("timp");
                int timpul = Integer.parseInt(timp);
                if(timpul > 0) {
                    txtTimp.setText(timp);
                    int taxa = timpul * 100;
                    txtTaxa.setText(String.valueOf(taxa));
                }
                else {
                    txtTimp.setText("0");
                    txtTaxa.setText("0");
                }
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Retur.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        }
    }//GEN-LAST:event_txtMidKeyPressed

