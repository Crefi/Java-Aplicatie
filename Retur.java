    public void Retur_Load() {
        int c;
        try {
            pst = con.prepareStatement("select * from retur");
            rs = pst.executeQuery();
            ResultSetMetaData rsd = (ResultSetMetaData) rs.getMetaData();
            c = rsd.getColumnCount();
            
            DefaultTableModel d = (DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            
            while (rs.next())
            {
                Vector v2 = new Vector();
                for(int i= 1; i<=c; i++)
                {
                   v2.add(rs.getString("id"));
                   v2.add(rs.getString("mId"));
                   v2.add(rs.getString("mNume"));
                   v2.add(rs.getString("cNume"));
                   v2.add(rs.getString("dataRetur"));
                   v2.add(rs.getString("timp"));
                   v2.add(rs.getString("taxa"));
                   
               
                   
                }
                d.addRow(v2);
            } 
        } catch (SQLException ex) {
            Logger.getLogger(Retur.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    
